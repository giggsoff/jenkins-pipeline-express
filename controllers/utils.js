function add(a, b) {
  return a + b;
}

function divide(a, b) {
  return a / b;
}

function multiply(a, b) {
  return a * b;
}

function subtract(a, b) {
  return a - b;
}

function sqr(a){
  return a*a;
}

module.exports = {
  add: add,
  divide: divide,
  multiply: multiply,
  subtract: subtract,
  sqr: sqr
};
